<h1>RGPD - Protection des données à caractère personnel</h1>
<p>
Lors de la participation aux activités en accès libre sur la plateforme <a href="http://concours.castor-informatique.fr">concours.castor-informatique.fr</a>,
comme les archives des concours Castor et Algoréa, ou bien d'autres parcours comme les parcours QuickPi, aucune donnée personnelle n'est collectée volontairement.
</p>
<p>
France-ioi s'engage à respecter toutes les lois et réglementations sur la protection des données collectées.
</p>
<h1>Données collectées, et finalité de la collecte</h1>

<p>Lors d'une participation individuelle à une activité en accès libre, les réponses des utilisateurs aux exercices sont collectées,
et rattachées à un code généré aléatoirement, qui est transmis à l'utilisateur.</p>

<p>La finalité de cette collecte est avant tout de permettre à l'utilisateur de poursuivre son activité plus tard en fournissant le code aléatoire qui lui avait été transmis.</p>
<p>Une finalité secondaire est la collecte de statistiques sur l'utilisation des contenus, la difficulté des exercices, etc. destinés à l'amélioration future de ces contenus.</p>

<p>Ces données ne contiennent aucune donnée personnelle, sauf si l'utilisateur choisit de lui-même de les inclure dans ses réponses aux exercices, bien que cela ne soit pas du tout demandé ni suggéré. Comme c'est cependant possible, nous traitons toutes les données enregistrées avec les mêmes conditions de sécurité que s'il s'agissait de données personnelles.</p>


<p>
Les données fournies par les utilisateurs sont enregistrées dans un fichier informatisé par l'association France-ioi.
</p>
<h1>Durée de conservation des données</h1>
<p>
Les réponses des élèves aux exercices sont conservées au maximum 18 mois.
</p>

<h1>Confidentialité et sécurité des données</h1>
<p>
Pendant cette période, nous mettons en place tous moyens aptes à assurer la confidentialité et la sécurité de ces données, de manière à empêcher leur endommagement, effacement ou accès par des tiers non autorisés.
</p>
<p>
L'accès à ces données est strictement limité à un maximum de 3 personnes travaillant pour l’association France-ioi, habilités en raison de leurs fonctions et tenus à une obligation de confidentialité : le président de l'association et deux développeurs responsables de la maintenance des serveurs..
</p>
<p>
France-ioi s'engage à ne pas vendre, louer, céder ou donner accès à des tiers aux données personnelles, à moins d’y être contraints en raison d’un motif légitime (obligation légale, lutte contre la fraude ou l’abus, exercice des droits de la défense, etc.).
</p>
<p>
Les données personnelles traitées dans le cadre de nos activités sont stockées uniquement sur des serveurs sécurisés de l’architecture du leader mondial d'hébergement de serveurs, AWS, localisés en europe. Les données ne seront jamais transférées vers un pays en dehors de l'Espace Économique Européen (EEE)
</p>
<p>
L’accès aux données personnelles se fait toujours par une connexion sécurisée.
</p>
<h1>Droits d’accès, rectification, suppression et portabilité</h1>
<p>
Conformément aux dispositions légales et réglementaires applicables, en particulier la loi n° 78-17 du 6 janvier 1978 modifiée relative à l'informatique, aux fichiers et aux libertés et du règlement européen n°2016/679/UE du 27 avril 2016 (applicable dès le 25 mai 2018), l'utilisateur dispose des droits suivants :
</p>
<ul>
   <li>Exercer son droit d’accès, pour connaître les données personnelles qui le concernent ;</li>
   <li>Demander la mise à jour de ses données, si celles-ci sont inexactes ;</li>
   <li>Demander la portabilité ou la suppression de ses données ;</li>
   <li>Demander la limitation du traitement de ses données ;</li>
   <li>S'opposer, pour des motifs légitimes, au traitement de ses données ;</li>
</ul>
<p>
Ces différents droits sont à exercer en nous contactant par courriel à l'adresse suivante : info@france-ioi.org.
</p>
<p>
Ne stockant pas d'informations personnelles, seul le code aléatoire transmis à l'utilisateur pourra nous être fourni, pour que nous puissions identifier quelles données sont concernées par la demande.
</p>
<h1>Utilisation de cookies</h1>
<p>
Aucun des sites rattachés au concours n'utilise de cookies de longue durée.
</p>
<p>
Ils n'utilisent que des cookies à durée très réduite (moins de 3h), avec pour unique rôle de permettre à l'utilisateur de pouvoir retrouver sa session en cas de fermeture de son navigateur par erreur, ou rechargement de la page.
</p>
<h1>Algorithmes de calcul des scores</h1>
<h3>Calcul du score</h3>
   <p>
   Lors du déroulement d'une participation, le score de chaque participant est calculé et affiché en permanence.
   </p>
   <p>
   Il s'agit simplement de la somme des points obtenus sur chaque sujet.
   </p>

